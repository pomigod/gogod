def count_cooking_methods():
    """
    조리법별 개수 카운트
    반찬명이 정확히 "조림", "볶음", "무침", "구이"로 끝나는 것만 카운트
    """
    counts = {"조림": 0, "볶음": 0, "무침": 0, "구이": 0}
    all_dishes = []

    # 5일간 5개 코너의 모든 반찬 수집
    for day in jan_13_week_lunch.values():
        for corner in ["한식A", "한식B", "양식", "팝업A", "팝업B"]:
            if corner in day:
                # 메인 메뉴도 포함 (문제에서 "제공된 반찬들"이라고 했으므로 메인 제외 가능성 있음)
                # 일단 반찬만 포함
                all_dishes.extend(day[corner]["반찬"])

    # 조리법별 카운트
    for dish in all_dishes:
        if dish.endswith("조림"):
            counts["조림"] += 1
        elif dish.endswith("볶음"):
            counts["볶음"] += 1
        elif dish.endswith("무침"):
            counts["무침"] += 1
        elif dish.endswith("구이"):
            counts["구이"] += 1

    return counts


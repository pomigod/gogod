# 조리법별 반찬 카운트
def count_cooking_methods():
    """
    반찬명이 정확히 "조림", "볶음", "무침", "구이"로 끝나는 개수
    """
    counts = {"조림": 0, "볶음": 0, "무침": 0, "구이": 0}

    all_dishes = []

    # 5일간 5개 코너(한식A, 한식B, 양식, 팝업A, 팝업B)의 모든 반찬 수집
    for day in jan_13_week_menus.values():
        for corner in ["한식A", "한식B", "양식", "팝업A", "팝업B"]:
            if corner in day and "구성" in day[corner]:
                all_dishes.extend(day[corner]["구성"])

    # 조리법별 카운트
    for dish in all_dishes:
        if dish.endswith("조림"):
            counts["조림"] += 1
        elif dish.endswith("볶음"):
            counts["볶음"] += 1
        elif dish.endswith("무침"):
            counts["무침"] += 1
        elif dish.endswith("구이"):
            counts["구이"] += 1

    return counts


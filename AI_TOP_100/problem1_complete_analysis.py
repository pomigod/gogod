def analyze_cooking_methods():
    """조리법별 개수 카운트"""
    counts = {"조림": 0, "볶음": 0, "무침": 0, "구이": 0}

    for day_data in jan_13_week_dishes.values():
        for corner in ["한식A", "한식B", "양식", "팝업A", "팝업B"]:
            for dish in day_data.get(corner, []):
                if dish.endswith("조림"):
                    counts["조림"] += 1
                elif dish.endswith("볶음"):
                    counts["볶음"] += 1
                elif dish.endswith("무침"):
                    counts["무침"] += 1
                elif dish.endswith("구이"):
                    counts["구이"] += 1

    # 내림차순 정렬
    sorted_methods = sorted(counts.items(), key=lambda x: x[1], reverse=True)
    print("문제 1-1: 조리법별 메뉴 분석 (내림차순)")
    print("=" * 50)
    for method, count in sorted_methods:
        print(f"{method}: {count}개")
    print()

    # 답안 형식
    answer = " > ".join([m[0] for m in sorted_methods])
    print(f"답안: {answer}")
    print()

    return counts

